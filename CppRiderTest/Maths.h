#pragma once

class Maths
{
public:

    long add(long x, long y)
    {
        return x + y;
    }

    long subtract(long x, long y)
    {
        return x + y;
    }

    long divide(long x, long y)
    {
        return x / y;
    }

    long multiply (long x, long y)
    {
        return x * y;
    }
};
